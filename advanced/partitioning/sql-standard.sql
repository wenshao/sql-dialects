-- SQL Standard: 表分区策略
--
-- 参考资料:
--   [1] ISO/IEC 9075-2:2023 - SQL Foundation
--       https://www.iso.org/standard/76584.html

-- SQL 标准没有定义表分区的语法
-- 分区是各数据库厂商独立实现的功能

-- ============================================================
-- 分区概念
-- ============================================================

-- 分区类型：
-- RANGE 分区    按值范围划分（如日期范围）
-- LIST 分区     按离散值列表划分（如地区）
-- HASH 分区     按哈希值划分（均匀分布）
-- KEY 分区      按数据库内部键划分
-- 复合分区       多种分区方式组合

-- ============================================================
-- 分区的优势
-- ============================================================

-- 1. 分区裁剪（Partition Pruning）：查询只扫描相关分区
-- 2. 独立维护：可以对单个分区进行操作（加载、删除、归档）
-- 3. 并行处理：分区间可以并行扫描
-- 4. 存储管理：不同分区可以放在不同存储介质

-- ============================================================
-- 各数据库的分区支持
-- ============================================================

-- 完整支持：Oracle, PostgreSQL 10+, MySQL 5.1+, SQL Server 2005+, DB2
-- 部分支持：SQLite（无）, Derby（无）, H2（无）
-- 列存储引擎：BigQuery, Snowflake, ClickHouse, Redshift 有自己的分区概念
-- 分布式系统：Spanner, CockroachDB 用分片代替分区

-- 注意：SQL 标准没有定义分区语法
-- 注意：各数据库的分区实现差异很大
-- 注意：分区裁剪是分区最重要的性能优势
-- 注意：选择正确的分区键是设计分区表的关键
