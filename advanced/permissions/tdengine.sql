-- ============================================================
-- 不支持的权限功能
-- ============================================================

-- 不支持角色（ROLE）
-- 不支持表级权限
-- 不支持列级权限
-- 不支持行级安全（RLS）
-- 不支持 GRANT OPTION
-- 不支持默认权限

-- 注意：权限粒度仅到数据库级别
-- 注意：只有 READ, WRITE, ALL 三种权限
-- 注意：不支持角色和细粒度权限
-- 注意：root 用户默认密码为 taosdata
