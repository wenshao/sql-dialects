-- 6. 外部表与多格式支持
-- ============================================================
CREATE TABLE external_events (
    id         BIGINT,
    event_type STRING,
    event_date DATE
) USING PARQUET LOCATION 's3://my-bucket/events/';

CREATE TABLE csv_data (col1 STRING, col2 INT)
USING CSV OPTIONS (header 'true', delimiter ',')
LOCATION 's3://my-bucket/csv-data/';

-- CTAS
CREATE OR REPLACE TABLE users_summary AS
SELECT city, COUNT(*) AS cnt, AVG(age) AS avg_age FROM users GROUP BY city;

-- ============================================================
-- 7. 横向对比: Databricks vs 其他引擎
-- ============================================================
-- 1. 存储层:
--   Databricks: Delta Lake（Parquet + 事务日志），ACID，Time Travel
--   Flink:      不存储数据（Connector 映射外部系统）
--   DuckDB:     自有列式存储格式（单文件）
--   Trino:      不存储数据（取决于 Connector: Hive/Iceberg/Delta）

-- 2. 数据维护:
--   Databricks: OPTIMIZE + VACUUM + Liquid Clustering（自动化程度最高）
--   Trino:      依赖底层存储的维护（Iceberg expireSnapshots/rewriteDataFiles）
--   Flink:      无维护概念（流式持续处理）
--   DuckDB:     无维护需求（嵌入式，文件紧凑）

-- 3. Time Travel:
--   Databricks: VERSION AS OF / TIMESTAMP AS OF（Delta Log 驱动）
--   Trino:      Iceberg 支持 FOR VERSION AS OF / FOR TIMESTAMP AS OF
--   DuckDB:     无 Time Travel
--   Flink:      无 Time Travel（流处理面向未来，不回溯）

