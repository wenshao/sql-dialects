-- 8. 版本演进
-- ============================================================
-- Databricks SQL (2020): 独立 SQL 端点，Photon C++ 引擎
-- Delta Lake 1.0 (2022): 开源版本，MERGE/UPDATE/DELETE
-- Unity Catalog (2022):  统一元数据治理
-- Liquid Clustering (2023): 取代分区 + Z-ORDER
-- Delta Lake 3.0 (2023):   UniForm（一份数据同时支持 Delta/Iceberg/Hudi 读取）
-- Databricks SQL 2024:     Serverless SQL Warehouse 默认，AI Functions
-- Unity Catalog 开源 (2024-06): 开源版本，支持多引擎
-- Delta Lake 4.0 (2025):   Variant 类型、增强的 Liquid Clustering
--
-- 对引擎开发者的参考:
--   Databricks 的 Lakehouse 路径: 先统一存储（Delta Lake）→ 再统一治理（Unity Catalog）
--   → 最后统一计算（Photon + SQL + AI）。
--   关键趋势: 表格式之争（Delta vs Iceberg vs Hudi）走向互通（UniForm）。
