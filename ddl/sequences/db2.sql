-- ============================================
-- 序列 vs 自增 权衡
-- ============================================
-- 1. IDENTITY：简单，绑定到表，一张表只能有一个
-- 2. SEQUENCE：灵活，可跨表共享，支持 ORDER 保证严格有序
-- 3. GENERATED ALWAYS：数据库完全控制值的生成
-- 4. GENERATED BY DEFAULT：允许手动指定，适合数据迁移
-- 5. GENERATE_UNIQUE()：适合需要唯一标识但不需要连续递增的场景
