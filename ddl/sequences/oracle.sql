-- 6. 总结: 自增策略选择矩阵
-- ============================================================
-- 场景                     推荐方式
-- 单表自增主键（简单）       IDENTITY (12c+)
-- 单表自增主键（灵活控制）   SEQUENCE + DEFAULT
-- 跨表共享序列号            SEQUENCE
-- RAC 环境严格有序           SEQUENCE + ORDER + CACHE
-- 全局唯一标识              SYS_GUID()
-- 分布式环境               UUID（避免全局序列的性能瓶颈）
