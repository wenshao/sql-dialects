-- ============================================
-- 序列 vs 自增 权衡
-- ============================================
-- SQL 标准定义了两种机制：
-- 1. SEQUENCE（SQL:2003）：独立对象，可跨表共享
-- 2. IDENTITY（SQL:2003）：列级自动生成
-- 3. GENERATED ALWAYS：数据库完全控制
-- 4. GENERATED BY DEFAULT：允许手动覆盖
-- 5. OVERRIDING SYSTEM VALUE / OVERRIDING USER VALUE 控制覆盖行为
