-- MySQL: Math Functions
--
-- 参考资料:
--   [1] MySQL 8.0 Reference Manual - Mathematical Functions
--       https://dev.mysql.com/doc/refman/8.0/en/mathematical-functions.html

-- ============================================================
-- 基本数学函数
-- ============================================================
SELECT ABS(-42);                          -- 42
SELECT CEIL(4.3);                         -- 5            (CEILING 同义)
SELECT CEILING(4.3);                      -- 5
SELECT FLOOR(4.7);                        -- 4
SELECT ROUND(3.14159, 2);                 -- 3.14
SELECT ROUND(3.14159);                    -- 3
SELECT TRUNCATE(3.14159, 2);              -- 3.14
SELECT TRUNCATE(3.14159, 0);              -- 3

-- ============================================================
-- 取模运算
-- ============================================================
SELECT MOD(17, 5);                        -- 2
SELECT 17 % 5;                            -- 2
SELECT 17 MOD 5;                          -- 2            (关键字形式)

-- ============================================================
-- 幂、根、指数、对数
-- ============================================================
SELECT POWER(2, 10);                      -- 1024         (POW 同义)
SELECT POW(2, 10);                        -- 1024
SELECT SQRT(144);                         -- 12
SELECT EXP(1);                            -- 2.718281828...
SELECT LN(2.718281828);                   -- ≈ 1.0
SELECT LOG(2.718281828);                  -- ≈ 1.0        (以 e 为底！不同于 PostgreSQL)
SELECT LOG(2, 1024);                      -- 10           (自定义底数)
SELECT LOG2(1024);                        -- 10           (以 2 为底)
SELECT LOG10(1000);                       -- 3

-- ============================================================
-- 符号和常量
-- ============================================================
SELECT SIGN(-42);                         -- -1
SELECT SIGN(0);                           -- 0
SELECT SIGN(42);                          -- 1
SELECT PI();                              -- 3.141593

-- ============================================================
-- 随机数
-- ============================================================
SELECT RAND();                            -- 0.0 到 1.0 之间
SELECT RAND(42);                          -- 可重复的随机数（给定种子）
SELECT FLOOR(RAND() * 100 + 1);          -- 1 到 100 的随机整数

-- ============================================================
-- 三角函数（弧度）
-- ============================================================
SELECT SIN(0);                            -- 0
SELECT COS(0);                            -- 1
SELECT TAN(PI()/4);                       -- ≈ 1.0
SELECT ASIN(1);                           -- π/2
SELECT ACOS(1);                           -- 0
SELECT ATAN(1);                           -- π/4
SELECT ATAN2(1, 1);                       -- π/4          (ATAN(y, x) 同义)
SELECT ATAN(1, 1);                        -- π/4

-- 弧度角度转换
SELECT DEGREES(PI());                     -- 180
SELECT RADIANS(180);                      -- π
SELECT COT(1);                            -- 0.6420...    (余切)

-- ============================================================
-- GREATEST / LEAST
-- ============================================================
SELECT GREATEST(1, 5, 3, 9, 2);          -- 9
SELECT LEAST(1, 5, 3, 9, 2);             -- 1
-- 注意：MySQL 中如果任一参数为 NULL，结果为 NULL

-- ============================================================
-- 位运算
-- ============================================================
SELECT 5 & 3;                             -- 1            (AND)
SELECT 5 | 3;                             -- 7            (OR)
SELECT 5 ^ 3;                             -- 6            (XOR)
SELECT ~5;                                -- 18446744073709551610 (64位 NOT)
SELECT 1 << 4;                            -- 16           (左移)
SELECT 16 >> 2;                           -- 4            (右移)
SELECT BIT_COUNT(7);                      -- 3            (二进制中 1 的个数)
SELECT BIT_AND(column_name) FROM t;       -- 聚合位 AND
SELECT BIT_OR(column_name) FROM t;        -- 聚合位 OR
SELECT BIT_XOR(column_name) FROM t;       -- 聚合位 XOR

-- ============================================================
-- 其他数学函数
-- ============================================================
SELECT CONV(255, 10, 16);                 -- 'FF'         (进制转换)
SELECT CONV('FF', 16, 10);               -- '255'
SELECT CRC32('hello');                    -- 907060870    (CRC32 校验值)

-- 版本说明：
--   MySQL 5.x+ : 所有基本数学函数
--   MySQL 8.0+ : 完整支持
-- 注意：MySQL 的 LOG(x) 以 e 为底（不同于 PostgreSQL 的以 10 为底）
-- 注意：^ 是 XOR 运算符（不是幂运算，幂用 POW/POWER）
-- 注意：TRUNCATE 而非 TRUNC
-- 注意：RAND(seed) 可设置种子实现可重复随机
-- 限制：无立方根函数
-- 限制：无 GCD/LCM
