-- PostgreSQL: Math Functions
--
-- 参考资料:
--   [1] PostgreSQL Documentation - Mathematical Functions
--       https://www.postgresql.org/docs/current/functions-math.html

-- ============================================================
-- 基本数学函数
-- ============================================================
SELECT ABS(-42);                          -- 42
SELECT CEIL(4.3);                         -- 5            (CEILING 同义)
SELECT CEILING(4.3);                      -- 5
SELECT FLOOR(4.7);                        -- 4
SELECT ROUND(3.14159, 2);                 -- 3.14
SELECT ROUND(3.14159);                    -- 3            (无精度参数取整数)
SELECT TRUNC(3.14159, 2);                 -- 3.14         (截断)
SELECT TRUNC(3.14159);                    -- 3

-- ============================================================
-- 取模运算
-- ============================================================
SELECT MOD(17, 5);                        -- 2
SELECT 17 % 5;                            -- 2            (运算符)
SELECT DIV(17, 5);                        -- 3            (整除)  -- 14+

-- ============================================================
-- 幂、根、指数、对数
-- ============================================================
SELECT POWER(2, 10);                      -- 1024
SELECT 2 ^ 10;                            -- 1024         (幂运算符)
SELECT |/ 144;                            -- 12           (平方根运算符)
SELECT SQRT(144);                         -- 12
SELECT ||/ 27;                            -- 3            (立方根运算符)
SELECT CBRT(27);                          -- 3            (立方根)
SELECT EXP(1);                            -- 2.718281828...
SELECT LN(2.718281828);                   -- ≈ 1.0
SELECT LOG(100);                          -- 2.0          (以 10 为底)
SELECT LOG(2, 1024);                      -- 10           (自定义底数)
SELECT LOG10(1000);                       -- 3.0          -- 12+

-- ============================================================
-- 符号和常量
-- ============================================================
SELECT SIGN(-42);                         -- -1
SELECT SIGN(0);                           -- 0
SELECT SIGN(42);                          -- 1
SELECT PI();                              -- 3.14159265358979

-- ============================================================
-- 随机数
-- ============================================================
SELECT RANDOM();                          -- 0.0 到 1.0 之间的随机数
SELECT FLOOR(RANDOM() * 100 + 1);        -- 1 到 100 的随机整数
SELECT SETSEED(0.5);                      -- 设置随机种子

-- ============================================================
-- 三角函数（弧度）
-- ============================================================
SELECT SIN(0);                            -- 0
SELECT COS(0);                            -- 1
SELECT TAN(PI()/4);                       -- ≈ 1.0
SELECT ASIN(1);                           -- π/2
SELECT ACOS(1);                           -- 0
SELECT ATAN(1);                           -- π/4
SELECT ATAN2(1, 1);                       -- π/4

-- ============================================================
-- 三角函数（角度版本）                                  -- 7.2+
-- ============================================================
SELECT SIND(90);                          -- 1            (角度正弦)
SELECT COSD(0);                           -- 1            (角度余弦)
SELECT TAND(45);                          -- ≈ 1.0        (角度正切)
SELECT ASIND(1);                          -- 90           (角度反正弦)
SELECT ACOSD(1);                          -- 0            (角度反余弦)
SELECT ATAND(1);                          -- 45           (角度反正切)
SELECT ATAN2D(1, 1);                      -- 45           (角度两参数反正切)

-- 弧度角度转换
SELECT DEGREES(PI());                     -- 180
SELECT RADIANS(180);                      -- π

-- ============================================================
-- 双曲函数                                             -- 12+
-- ============================================================
SELECT SINH(1);                           -- 1.1752...
SELECT COSH(1);                           -- 1.5430...
SELECT TANH(1);                           -- 0.7615...
SELECT ASINH(1);                          -- 0.8813...
SELECT ACOSH(1);                          -- 0
SELECT ATANH(0.5);                        -- 0.5493...

-- ============================================================
-- GREATEST / LEAST
-- ============================================================
SELECT GREATEST(1, 5, 3, 9, 2);          -- 9
SELECT LEAST(1, 5, 3, 9, 2);             -- 1
-- 注意：任一参数为 NULL 则返回 NULL

-- ============================================================
-- 位运算
-- ============================================================
SELECT 5 & 3;                             -- 1            (AND)
SELECT 5 | 3;                             -- 7            (OR)
SELECT 5 # 3;                             -- 6            (XOR, PostgreSQL 用 #)
SELECT ~5;                                -- -6           (NOT)
SELECT 1 << 4;                            -- 16           (左移)
SELECT 16 >> 2;                           -- 4            (右移)
SELECT BIT_AND(column_name) FROM t;       -- 聚合位 AND
SELECT BIT_OR(column_name) FROM t;        -- 聚合位 OR

-- ============================================================
-- 其他数学函数
-- ============================================================
SELECT FACTORIAL(5);                      -- 120          (阶乘)   -- 已弃用
SELECT 5!;                                -- 120          (阶乘运算符，已弃用)
SELECT GCD(12, 18);                       -- 6            (最大公约数)  -- 13+
SELECT LCM(12, 18);                       -- 36           (最小公倍数)  -- 13+
SELECT MIN_SCALE(12.3400);                -- 2            (最小标度)   -- 13+
SELECT TRIM_SCALE(12.3400);               -- 12.34        (去除尾零)   -- 13+
SELECT SCALE(123.456);                    -- 3            (小数位数)

-- 版本说明：
--   PostgreSQL 7.x+  : 基本数学函数、三角函数（弧度）
--   PostgreSQL 7.2+  : 角度三角函数 (SIND, COSD 等)
--   PostgreSQL 12+   : 双曲函数, LOG10
--   PostgreSQL 13+   : GCD, LCM, MIN_SCALE, TRIM_SCALE
--   PostgreSQL 14+   : DIV 函数
-- 注意：PostgreSQL 的 LOG(x) 默认以 10 为底（不同于数学约定）
-- 注意：^ 是幂运算符（不是 XOR，XOR 用 #）
-- 注意：RANDOM() 返回 [0.0, 1.0) 范围
-- 限制：TRUNCATE 写作 TRUNC
