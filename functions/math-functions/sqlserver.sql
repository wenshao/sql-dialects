-- SQL Server: Math Functions
--
-- 参考资料:
--   [1] SQL Server T-SQL - Mathematical Functions
--       https://learn.microsoft.com/en-us/sql/t-sql/functions/mathematical-functions-transact-sql

-- ============================================================
-- 基本数学函数
-- ============================================================
SELECT ABS(-42);                          -- 42
SELECT CEILING(4.3);                      -- 5            (无 CEIL 别名)
SELECT FLOOR(4.7);                        -- 4
SELECT ROUND(3.14159, 2);                 -- 3.14
SELECT ROUND(3.14159, 2, 1);             -- 3.14         (第3参数=1: 截断)
SELECT ROUND(3.14159, 0);                 -- 3

-- ============================================================
-- 取模运算
-- ============================================================
SELECT 17 % 5;                            -- 2            (运算符)
-- 无 MOD() 函数

-- ============================================================
-- 幂、根、指数、对数
-- ============================================================
SELECT POWER(2, 10);                      -- 1024
SELECT SQRT(144);                         -- 12
SELECT SQUARE(12);                        -- 144          (平方, SQL Server 特有)
SELECT EXP(1);                            -- 2.718281828...
SELECT LOG(2.718281828);                  -- ≈ 1.0        (自然对数)
SELECT LOG(1024, 2);                      -- 10           (自定义底数)  -- 2012+
SELECT LOG10(1000);                       -- 3

-- ============================================================
-- 符号和常量
-- ============================================================
SELECT SIGN(-42);                         -- -1
SELECT SIGN(0);                           -- 0
SELECT SIGN(42);                          -- 1
SELECT PI();                              -- 3.14159265358979

-- ============================================================
-- 随机数
-- ============================================================
SELECT RAND();                            -- 0.0 到 1.0 之间
SELECT RAND(42);                          -- 可重复随机数
SELECT ABS(CHECKSUM(NEWID())) % 100 + 1; -- 行级随机整数（1-100）

-- ============================================================
-- 三角函数（弧度）
-- ============================================================
SELECT SIN(0);                            -- 0
SELECT COS(0);                            -- 1
SELECT TAN(PI()/4);                       -- ≈ 1.0
SELECT ASIN(1);                           -- π/2
SELECT ACOS(1);                           -- 0
SELECT ATAN(1);                           -- π/4
SELECT ATN2(1, 1);                        -- π/4          (注意: ATN2 不是 ATAN2)
SELECT COT(1);                            -- 0.6420...    (余切)

-- 弧度角度转换
SELECT DEGREES(PI());                     -- 180
SELECT RADIANS(180.0);                    -- π

-- ============================================================
-- GREATEST / LEAST                                    -- 2022+
-- ============================================================
SELECT GREATEST(1, 5, 3, 9, 2);          -- 9            -- 2022+
SELECT LEAST(1, 5, 3, 9, 2);             -- 1            -- 2022+
-- 2022 之前使用 IIF 或 CASE 模拟:
SELECT IIF(5 > 3, 5, 3);                 -- 5

-- ============================================================
-- 位运算
-- ============================================================
SELECT 5 & 3;                             -- 1            (AND)
SELECT 5 | 3;                             -- 7            (OR)
SELECT 5 ^ 3;                             -- 6            (XOR)
SELECT ~5;                                -- -6           (NOT)
-- 无 << >> 运算符，使用 POWER:
SELECT 1 * POWER(2, 4);                  -- 16           (模拟左移)

-- ============================================================
-- 其他数学函数
-- ============================================================
SELECT CHOOSE(2, 'a', 'b', 'c');         -- 'b'          (按索引选择)
SELECT IIF(1 > 0, 'yes', 'no');          -- 'yes'

-- 版本说明：
--   SQL Server 2005+ : 基本数学函数
--   SQL Server 2012+ : LOG(x, base) 两参数版本
--   SQL Server 2022+ : GREATEST / LEAST
-- 注意：SQL Server 用 CEILING（不支持 CEIL 缩写）
-- 注意：用 ATN2（不是 ATAN2）
-- 注意：^ 是 XOR（幂用 POWER）
-- 注意：SQUARE() 是 SQL Server 特有函数
-- 注意：ROUND(x, n, 1) 第三参数用于截断模式
-- 限制：无 TRUNC/TRUNCATE（使用 ROUND(x, n, 1)）
-- 限制：2022 之前无 GREATEST/LEAST
-- 限制：无 MOD() 函数
