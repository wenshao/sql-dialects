-- MySQL 兼容数学函数
SELECT ABS(-42); SELECT CEIL(4.3); SELECT CEILING(4.3); SELECT FLOOR(4.7);
SELECT ROUND(3.14159, 2); SELECT TRUNCATE(3.14159, 2);
SELECT MOD(17, 5); SELECT 17 % 5; SELECT 17 MOD 5;
SELECT POWER(2, 10); SELECT POW(2, 10); SELECT SQRT(144);
SELECT EXP(1); SELECT LN(EXP(1)); SELECT LOG(EXP(1)); SELECT LOG(2, 1024);
SELECT LOG2(1024); SELECT LOG10(1000);
SELECT SIGN(-42); SELECT PI();
SELECT RAND(); SELECT RAND(42);

SELECT SIN(0); SELECT COS(0); SELECT TAN(0);
SELECT ASIN(1); SELECT ACOS(1); SELECT ATAN(1); SELECT ATAN(1, 1); SELECT ATAN2(1, 1);
SELECT COT(1);
SELECT DEGREES(PI()); SELECT RADIANS(180);

SELECT GREATEST(1, 5, 3); SELECT LEAST(1, 5, 3);

SELECT 5 & 3; SELECT 5 | 3; SELECT 5 ^ 3; SELECT ~5;
SELECT 1 << 4; SELECT 16 >> 2;
SELECT BIT_COUNT(7);

SELECT CONV(255, 10, 16); SELECT CRC32('hello');

-- 注意：TiDB 兼容 MySQL 数学函数
-- 注意：LOG(x) 以 e 为底
-- 注意：^ 是 XOR（幂用 POW/POWER）
