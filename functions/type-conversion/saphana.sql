SELECT CAST(42 AS VARCHAR(10)) FROM DUMMY; SELECT CAST('42' AS INTEGER) FROM DUMMY;
SELECT CAST('3.14' AS DECIMAL(10,2)) FROM DUMMY; SELECT CAST('2024-01-15' AS DATE) FROM DUMMY;

-- TO_* 转换函数
SELECT TO_VARCHAR(42) FROM DUMMY;
SELECT TO_INTEGER('42') FROM DUMMY;
SELECT TO_DECIMAL('3.14', 10, 2) FROM DUMMY;
SELECT TO_DATE('2024-01-15', 'YYYY-MM-DD') FROM DUMMY;
SELECT TO_TIMESTAMP('2024-01-15 10:30:00', 'YYYY-MM-DD HH24:MI:SS') FROM DUMMY;
SELECT TO_NVARCHAR(42) FROM DUMMY;

-- 格式化
SELECT TO_VARCHAR(CURRENT_DATE, 'YYYY-MM-DD') FROM DUMMY;
SELECT TO_VARCHAR(123456.789, '999,999.99') FROM DUMMY;

-- 注意：SAP HANA 使用 TO_* 系列转换函数
-- 注意：需要 FROM DUMMY
-- 限制：无 TRY_CAST, ::, CONVERT (SQL Server 风格)
