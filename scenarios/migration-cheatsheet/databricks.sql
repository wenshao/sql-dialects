-- 一、数据类型: 兼容 Spark SQL + Delta Lake
--   INT→INT, BIGINT→BIGINT/LONG, FLOAT→FLOAT, DOUBLE→DOUBLE,
--   VARCHAR→STRING, DECIMAL→DECIMAL(p,s), BOOLEAN→BOOLEAN,
--   DATE→DATE, TIMESTAMP→TIMESTAMP, BINARY→BINARY,
--   JSON→STRING(用from_json), ARRAY→ARRAY<T>, MAP→MAP<K,V>, STRUCT→STRUCT<...>
-- 二、函数: 与Spark SQL完全兼容, 额外支持Delta Lake特有函数
-- 三、陷阱: Delta Lake是默认表格式, Unity Catalog管理权限,
--   AUTO_INCREMENT→GENERATED ALWAYS AS IDENTITY(Delta Lake),
--   支持MERGE INTO(很强大), 支持CLONE, 支持Time Travel
-- 四、自增: GENERATED ALWAYS AS IDENTITY 或 BIGINT GENERATED BY DEFAULT AS IDENTITY
-- 五、日期: current_timestamp(); current_date();
--   date_add(d,1); datediff(a,b); date_format(ts,'yyyy-MM-dd HH:mm:ss')
-- 六、字符串: length, upper, lower, trim, substring, regexp_replace, locate, concat, concat_ws
