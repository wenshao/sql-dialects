-- 一、数据类型（到 SAP HANA）
--   INT→INTEGER, BIGINT→BIGINT, FLOAT→REAL, DOUBLE→DOUBLE,
--   VARCHAR→NVARCHAR(n)(Unicode默认), TEXT→NCLOB, DECIMAL→DECIMAL(p,s),
--   BOOLEAN→BOOLEAN, DATE→DATE, TIMESTAMP→TIMESTAMP,
--   BLOB→BLOB, JSON→NCLOB(用JSON函数), AUTO_INCREMENT→GENERATED ALWAYS AS IDENTITY
-- 二、函数: IFNULL→IFNULL/COALESCE, NOW()→CURRENT_TIMESTAMP,
--   CONCAT→||或CONCAT, GROUP_CONCAT→STRING_AGG
-- 三、陷阱: 列存储为默认(行存储需显式指定), 内存数据库(数据量受内存限制),
--   NVARCHAR默认(Unicode), 计算视图/分析视图是HANA特色, 多租户架构
-- 四、自增: GENERATED ALWAYS AS IDENTITY 或 SEQUENCE
-- 五、日期: CURRENT_TIMESTAMP; CURRENT_DATE; ADD_DAYS(d,1);
--   DAYS_BETWEEN(a,b); TO_VARCHAR(ts,'YYYY-MM-DD HH24:MI:SS')
-- 六、字符串: LENGTH, UPPER, LOWER, TRIM, SUBSTR, REPLACE, LOCATE, ||, STRING_AGG
