-- ============================================================
-- 1. 整数类型
-- ============================================================

-- SMALLINT / INT2:  2 字节，-32768 ~ 32767
-- INTEGER  / INT4:  4 字节，-2^31 ~ 2^31-1
-- BIGINT   / INT8:  8 字节，-2^63 ~ 2^63-1

CREATE TABLE examples (
    small_val SMALLINT,
    int_val   INTEGER,
    big_val   BIGINT
);

-- 设计特点:
--   (a) 无 UNSIGNED 类型: PostgreSQL 设计者认为 UNSIGNED 增加类型复杂度，
--       BIGINT (8字节) 的范围已足够覆盖 MySQL UNSIGNED INT 的使用场景。
--       对比: MySQL 有 UNSIGNED（最大值翻倍），PostgreSQL 用更大类型替代。
--   (b) 无 TINYINT: PostgreSQL 没有 1 字节整数类型。
--       替代: SMALLINT (2字节)，或布尔值用 BOOLEAN。
--   (c) 无 MEDIUMINT: MySQL 有 3 字节 MEDIUMINT，PostgreSQL 没有。
--       PostgreSQL 不做"节省字节"的类型细分——简化类型系统。

-- ============================================================
-- 2. 自增序列类型
-- ============================================================

-- SERIAL 语法糖（传统方式）
CREATE TABLE t1 (id SERIAL PRIMARY KEY);       -- 4 字节
CREATE TABLE t2 (id BIGSERIAL PRIMARY KEY);    -- 8 字节

-- IDENTITY（10+，SQL 标准，推荐）
CREATE TABLE t3 (
    id  INTEGER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    id2 INTEGER GENERATED BY DEFAULT AS IDENTITY
);

